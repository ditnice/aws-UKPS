namespace UKPS.Api.Persistence.Enums;

/// <summary>
/// Represents the resolution status of an item or issue.
/// </summary>
public enum ResolutionStatus
{
    /// <summary>
    /// Indicates that the item or issue is unresolved.
    /// </summary>
    Unresolved = 0,

    /// <summary>
    /// Indicates that the item or issue has been resolved.
    /// </summary>
    Resolved = 1,
}
