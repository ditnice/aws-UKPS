/// Represents the possible outcomes of a QA (Quality Assurance) process.
/// </summary>
public enum QaOutcome
{
    /// <summary>
    /// Indicates that the QA process has been rejected.
    /// </summary>
    Rejected = 0,

    /// <summary>
    /// Indicates that the QA process has been approved.
    /// </summary>
    Approved = 1,
}
